// Contract codegen. Sources of truth (all post-integration, on rc/v0.1):
//   1. ../schemas/*.schema.json           → types via json-schema-to-typescript
//   2. ../docs/rfc-002-engine-design.md   → §3 canonical state tables + §2.2 closed
//      column sets, parsed into enums/edges/legality maps. RFC-002 §3 states that
//      SDK enums "are generated from or validated against this section"; schemas/README
//      pins the record-schema deferral and mandates generation from the ratified table,
//      never hand-copying.
// Output is committed under src/shared/contracts/generated/; --check fails on drift.
// contracts/schema-pins.json records the SHA-256 of every consumed source.
